#25. Armazene valores dos lados de um triângulo em variáveis e exiba-os.

print("Digite as medidas em centímetros.")
altura = int(input("Digite a medida da altura do triângulo:"))
base = int(input("Digite a medida da base do triângulo:"))

area = (base * altura) / 2

print("A área do triângulo é igual a:",area,"cm³")
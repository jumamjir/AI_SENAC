#2.Leia dois números inteiros e mostre a soma, subtração, multiplicação e divisão
# entre eles.

num1 = int(input("Informe o primeiro número: "))
num2 = int(input("Informe o segundo número: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print("A soma é igual a:",soma)
print("A subracao é igual a:",subtracao)
print("A multiplicacao é igual a",multiplicacao)
print("A divisao é igual a",divisao)



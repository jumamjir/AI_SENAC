#2 - Faça um programa que leia 2 números e mostre qual o menor e qual o maior.

num1 = int(input("Digite um número:"))
num2 = int(input("Digite um número:"))

if(num1 > num2):
    print("O número maior é:",num1, "e o menor número é:",num2)
else:
    print("O número maior é:",num2, "e o menor número é:",num1)




